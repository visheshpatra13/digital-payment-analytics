-- 5. Dynamic data masking for PII

USE ROLE SECURITYADMIN;

CREATE OR REPLACE MASKING POLICY MASK_EMAIL AS
  (VAL STRING) RETURNS STRING ->
    CASE
      WHEN CURRENT_ROLE() IN ('ACCOUNTADMIN', 'SECURITYADMIN', 'PAYMENTS_ANALYST') THEN VAL
      ELSE REGEXP_REPLACE(VAL, '(^.).+(@.+$)', '\\1***\\2')
    END;

ALTER TABLE DIM_CUSTOMER
  MODIFY COLUMN EMAIL SET MASKING POLICY MASK_EMAIL;

-- 6. Row Access Policy example (governance)

CREATE OR REPLACE ROW ACCESS POLICY RAP_COUNTRY_FILTER AS
  (COUNTRY_CODE STRING) RETURNS BOOLEAN ->
    CASE
      WHEN CURRENT_ROLE() = 'ACCOUNTADMIN' THEN TRUE
      WHEN CURRENT_ROLE() = 'PAYMENTS_ANALYST' THEN TRUE
      WHEN CURRENT_ROLE() = 'PAYMENTS_READONLY' THEN COUNTRY_CODE = 'US'
      ELSE FALSE
    END;

ALTER TABLE DIM_CUSTOMER
  ADD ROW ACCESS POLICY RAP_COUNTRY_FILTER ON (COUNTRY_CODE);

