USE DATABASE PAYMENTS_DWH;
USE SCHEMA CORE;

-- 1. Semi-structured data: store raw gateway payloads

CREATE OR REPLACE TABLE RAW_GATEWAY_EVENTS (
    EVENT_ID       VARCHAR      NOT NULL,
    RECEIVED_AT    TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP(),
    GATEWAY_NAME   VARCHAR,
    PAYLOAD        VARIANT      -- JSON from payment gateway
);

-- Example query parsing JSON
SELECT
    EVENT_ID,
    RECEIVED_AT,
    PAYLOAD:"transaction_id"::string      AS transaction_id,
    PAYLOAD:"card":{"bin"}::string       AS card_bin,
    PAYLOAD:"amount"::number(18,2)       AS amount,
    PAYLOAD:"currency"::string           AS currency
FROM RAW_GATEWAY_EVENTS
WHERE PAYLOAD:"status"::string = 'CAPTURED';

